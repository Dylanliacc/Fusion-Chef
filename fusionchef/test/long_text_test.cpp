#include <Arduino.h>
#include "../src/screen_control/screen_manager.h"

void testLongTextDisplay() {
    String longText = "第一章 引言\n\n"
        "在一个风和日丽的早晨，微风轻拂过城市的街道。这是一个关于技术与人性的故事，"
        "讲述了人工智能如何改变了我们的生活方式。随着科技的发展，我们的生活变得越来越便捷，"
        "但同时也带来了许多新的挑战。\n\n"
        "第二章 人工智能的崛起\n\n"
        "在过去的十年里，人工智能技术取得了突飞猛进的发展。从简单的规则系统到复杂的神经网络，"
        "从基础的模式识别到高级的自然语言处理，人工智能正在各个领域展现出其强大的潜力。\n\n"
        "第三章 技术的双刃剑\n\n"
        "科技发展带来便利的同时，也产生了一些负面影响。如何在享受科技红利的同时避免其带来的问题，"
        "成为了现代社会面临的重要课题。我们需要在发展与规范之间找到平衡点。\n\n"
        "第四章 未来展望\n\n"
        "展望未来，我们可以预见人工智能将在更多领域发挥作用。从医疗诊断到自动驾驶，"
        "从智能制造到个人助理，人工智能将继续改变我们的生活方式。但关键是要确保这种改变是正向的、"
        "可控的。\n\n"
        "第五章 结论\n\n"
        "技术进步是不可阻挡的潮流，但如何引导这股潮流走向正确的方向，需要我们共同的智慧和努力。"
        "让我们携手共建一个科技与人文共融的美好未来。";

    // 注册长文本显示页面
    ScreenManager::getInstance().registerPage(PageType::LONG_TEXT, "测试长文本", nullptr);
    
    // 设置长文本内容
    ScreenManager::getInstance().setLongText(longText, "长文本测试");
    
    // 切换到长文本页面
    ScreenManager::getInstance().switchScreen(3); // 假设长文本页面索引为3
}